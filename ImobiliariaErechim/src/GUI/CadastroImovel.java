    public int pegaAutoIncremento() {
        String sql = "SELECT MAX(CODIGO) FROM imovel";

        try {
            PreparedStatement stmt = conexao.prepareStatement(sql);

            ResultSet res = stmt.executeQuery(sql);
            while (res.next()) {
                codigo = res.getInt(1);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro SQL: " + e, "Erro", 1);
        }
        return codigo + 1;
    }

    public void CadastraImovel(int dormitorios, int banheiros, int suites, int vaga_garagem, String endereco, String bairro,
            String cidade, double valor, InputStream i1, InputStream i2, InputStream i3, InputStream i4, double area_total, double area_construida, String comentarios) {
        String sql = "INSERT INTO movel (DORMITORIOS,BANHEIROS,SUITES,VAGA_GARAGEM,ENDERECO,BAIRRO,CIDADE,VALOR,FOTO_01,FOTO_02,FOTO_03,FOTO_04,AREA_TOTAL,AREA_CONSTRUIDA,COMENTARIOS,INFRA_ESTRUTURA)"
                + "VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?) ";
        try {
            PreparedStatement stmt = conexao.prepareStatement(sql);
            stmt.setInt(1, dormitorios);
            stmt.setInt(2, banheiros);
            stmt.setInt(3, suites);
            stmt.setInt(4, vaga_garagem);
            stmt.setString(5, endereco);
            stmt.setString(6, bairro);
            stmt.setString(7, cidade);
            stmt.setDouble(8, valor);
            stmt.setBinaryStream(9, i1, arq.length());
            stmt.setBinaryStream(10, i1, arq.length());
            stmt.setBinaryStream(11, i1, arq.length());
            stmt.setBinaryStream(12, i1, arq.length());
            stmt.setDouble(13, area_total);
            stmt.setDouble(14, area_construida);
            stmt.setString(15, comentarios);

            stmt.execute();
            JOptionPane.showMessageDialog(null, "Cadastrado");
            jLabel2.setText(String.valueOf(pegaAutoIncremento()));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

