    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int dormitorios, banheiros, suites, vaga_garagem;
        String endereco, bairro, comentarios, infra_estrutura;
        double valor, area_total, area_construida;

        dormitorios = Integer.parseInt(jTextField1.getText());
        banheiros = Integer.parseInt(jTextField2.getText());
        suites = Integer.parseInt(jTextField3.getText());
        vaga_garagem = suites = Integer.parseInt(jTextField4.getText());
        bairro = jTextField5.getText();
        endereco = jTextField10.getText();
        valor = Double.parseDouble(jTextField6.getText());
        area_total = Double.parseDouble(jTextField7.getText());
        area_construida = Double.parseDouble(jTextField8.getText());
        infra_estrutura = jTextField9.getText();
        comentarios = jTextArea1.getText();

        if ((endereco.isEmpty()) || (valor == 0.0 || (area_total == 0.0) || (dormitorios == 0.0) || (banheiros == 0.0))) {
            JOptionPane.showMessageDialog(null, "Campos vazios!");
        }
        CadastraImovel(dormitorios, banheiros, suites, vaga_garagem, endereco, bairro, cidade, valor, null, null, null, null, area_total, area_construida, comentarios);
        LimparCampos();

    }//GEN-LAST:event_jButton3ActionPerformed

