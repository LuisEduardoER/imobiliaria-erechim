            public void listarBanco(){
        ListarCidades lista_cidades_tablemodel = new ListarCidades(lista_cidades);
        String sql = "SELECT * FROM cidades";
        this.conexao = (Connection) new Login().getConnection();
        String nome,estado;
        double renda;
        int id,x=0;
        
        try{
            
            java.sql.PreparedStatement stmt = (java.sql.PreparedStatement) conexao.prepareStatement(sql);
            
            ResultSet rs = stmt.executeQuery();
            
            while (rs.next()){
                id=rs.getInt("ID_CIDADE");
                nome=rs.getString("NOME");
                estado=rs.getString("ESTADO");
                
                Cidades cidades = new Cidades();
                cidades.setId(id);
                cidades.setNome(nome);
                cidades.setEstado(estado);
                
                
                lista_cidades_tablemodel.addCidades(cidades);
                jTable1.setModel(lista_cidades_tablemodel);
                jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);  
                jTable1.getColumnModel().getColumn(1).setPreferredWidth(80);   
                jTable1.getColumnModel().getColumn(2).setPreferredWidth(5);
                
            }
            
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
              public void PesquisarCidade(String nome){
        String sql = "SELECT * FROM cidades USE INDEX(iNome) where nome like '%"+nome+"%'";
        String nomeRetirado,estado;
        int codigo;
        ListarCidades lista_cidades_tablemodel = new ListarCidades(lista_cidades);
        try{
       
            PreparedStatement stmt = (PreparedStatement) conexao.prepareStatement(sql);
            
            ResultSet rs = stmt.executeQuery();
            
            while (rs.next()){
                
                nomeRetirado = rs.getString("nome");
                estado  = rs.getString("estado");
                codigo = rs.getInt("id_cidade");
                
               Cidades cidades = new Cidades();
               cidades.setId(codigo);
               cidades.setNome(nomeRetirado);
               cidades.setEstado(estado);
               
                //lista_cidades.add(cidades);
               
                lista_cidades_tablemodel.addCidades(cidades);
                jTable1.setModel(lista_cidades_tablemodel);
                jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);  
                jTable1.getColumnModel().getColumn(1).setPreferredWidth(280);   
                jTable1.getColumnModel().getColumn(2).setPreferredWidth(50);
                
                
                CadastroLocatario.jTextField4.setText(nomeRetirado);
            }
              
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }      
            
